/**
 * @file calc_advance.h
 * @brief Library for advanced calculation operations
 */

 #ifndef _CALC_ADVANCE_H
 #define _CALC_ADVANCE_H
 
 /**
  * @brief Calculate power of a base number
  * @param base Base integer
  * @param exp Exponent (power value)
  * @return Result of base raised to the power of exp
  */
 int power(int base, int exp);
 
 /**
  * @brief Calculate factorial of a number
  * @param n Integer to calculate factorial
  * @return Factorial value as long long integer
  */
 long long factorial(int n);
 
 #endif